#include <stdio.h>
#include <string.h>
#include "fenix_opt.h"
#include "fenix_util.h"
#include "fenix_ext.h"

#define DEBUG 1




/**
 * @brief
 * @param argc
 * @param argv
 * @param opts
 */
void __fenix_init_opt(int argc, char **argv) {
   int i;

   /* Initalize the value */

   fenix.options.verbose = -1;

   for( i = 0; i < argc; i++ )
   {

      if( strcmp(argv[i],"--fenix_v") == 0 || strcmp(argv[i],"--FENIX_V") == 0 )
      {
          printf("Inside if %d\n",i);
         if( i+1 < argc )
         {
            fenix.options.verbose = atoi(argv[i+1]);
         }
      }
    }
}
